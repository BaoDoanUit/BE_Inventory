using MineralInventory.Models;
using Dapper;
using Npgsql;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Data;

namespace  MineralInventory.Respository
{
    public class TypePacketDAL : BaseDAL
    {
        public bool InsertTypePacket(TypePacketInfo model)
        {
            using(var conn = new NpgsqlConnection(GetConnection()))
            {
                try
                {
                    if(conn.State  != ConnectionState.Open)
                        conn.Open();
                    string command  = "call insert_type_Packet(@name,@code)";
                    DynamicParameters param = new DynamicParameters();
              
                    param.Add("@name",model.NameTypePacket.Trim());
                    param.Add("@code",model.CodeTypePacket.Trim());
                 
                    conn.Execute(command,param);
                    return true;
                }
                catch (Exception e)
                {
                    Console.WriteLine(e);
                    return false;
                }finally
                {
                    if(conn.State != ConnectionState.Closed)
                        conn.Close();
                }
               

            }
        }
